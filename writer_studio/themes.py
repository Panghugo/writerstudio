def build_style(theme_name=DEFAULT_THEME, author_name=None):
    theme = theme_name if theme_name in THEMES else DEFAULT_THEME
    style = THEMES[theme].copy()
    style["canvas_width"] = DEFAULT_CANVAS_WIDTH

    if author_name:
        if "{author}" in style.get("author_text", ""):
            style["author_text"] = style["author_text"].replace("{author}", author_name)
        if "{author}" in style.get("cover_meta_text", ""):
            style["cover_meta_text"] = style["cover_meta_text"].replace("{author}", author_name)

    return style
